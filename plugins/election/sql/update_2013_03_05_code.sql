INSERT INTO incident_category(incident_id, category_id)
SELECT incident.id AS incident_id, `code`.category_id
FROM incident
INNER JOIN message ON (message.incident_id = incident.id)
INNER JOIN monitor ON (monitor.phonenumber = message.message_from)
INNER JOIN `code` ON (incident.incident_title = `code`.`code_description`)
WHERE `code`.`category_id` IS NOT NULL
AND NOT EXISTS (SELECT id FROM incident_category WHERE incident_id = incident.id AND category_id = `code`.`category_id`);